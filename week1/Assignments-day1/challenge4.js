// finding the smallest element in a given array
let marks=[1,20,30,40,2]

for(let i=0;i<marks.length;i++)
{
    let smallest=marks[0];
    if(marks[i]<smallest)
    {
        smallest=marks[i];
        console.log(smallest);
        
    }
    else
    {
        console.log(smallest);
        
    }
}